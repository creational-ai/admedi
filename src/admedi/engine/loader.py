def load_profiles(
    profiles_path: str | Path = "profiles.yaml",
) -> dict[str, Profile]:
    """Load and validate app profiles from ``profiles.yaml``.

    Reads the ``profiles`` top-level key from the YAML file and returns
    a dict mapping alias to ``Profile``. Each alias entry must provide
    ``app_key`` (non-empty string), ``app_name`` (string), and
    ``platform`` (valid ``Platform`` enum value).

    Args:
        profiles_path: Path to the profiles YAML file. Defaults to
            ``"profiles.yaml"`` (project root convention).

    Returns:
        A dict mapping alias (str) to ``Profile`` instance.

    Raises:
        FileNotFoundError: If the profiles file does not exist.
        ConfigValidationError: If the YAML is malformed, the ``profiles``
            key is missing, or an alias entry is missing required fields
            or has invalid values.

    Examples:
        >>> profiles = load_profiles("profiles.yaml")  # doctest: +SKIP
        >>> profiles["hexar-ios"].app_key  # doctest: +SKIP
        '676996cd'
        >>> profiles["hexar-ios"].platform  # doctest: +SKIP
        <Platform.IOS: 'iOS'>
    """
    file_path = Path(profiles_path)

    if not file_path.exists():
        raise FileNotFoundError(
            f"Profiles file not found: {file_path}"
        )

    yaml = YAML(typ="safe")

    try:
        raw_data = yaml.load(file_path)
    except YAMLError as exc:
        raise ConfigValidationError(
            f"Malformed YAML in '{file_path.name}': {exc}",
            detail=str(exc),
        ) from exc

    if raw_data is None or not isinstance(raw_data, dict):
        raise ConfigValidationError(
            f"Profiles file '{file_path.name}' must contain a YAML mapping"
        )

    if "profiles" not in raw_data:
        raise ConfigValidationError(
            f"Profiles file '{file_path.name}' is missing required 'profiles' key"
        )

    profiles_dict = raw_data["profiles"]

    if not isinstance(profiles_dict, dict):
        raise ConfigValidationError(
            f"'profiles' key in '{file_path.name}' must be a mapping, "
            f"got {type(profiles_dict).__name__}"
        )

    result: dict[str, Profile] = {}

    for alias, entry in profiles_dict.items():
        if not isinstance(entry, dict):
            raise ConfigValidationError(
                f"Profile '{alias}' in '{file_path.name}' must be a mapping "
                f"with app_key, app_name, and platform fields"
            )

        # Validate required fields
        if "app_key" not in entry or not entry["app_key"]:
            raise ConfigValidationError(
                f"Profile '{alias}' in '{file_path.name}' is missing "
                f"required 'app_key' field (must be a non-empty string)"
            )

        if not isinstance(entry["app_key"], str):
            raise ConfigValidationError(
                f"Profile '{alias}' in '{file_path.name}' has invalid "
                f"'app_key': must be a string, got {type(entry['app_key']).__name__}"
            )

        if "app_name" not in entry:
            raise ConfigValidationError(
                f"Profile '{alias}' in '{file_path.name}' is missing "
                f"required 'app_name' field"
            )

        if not isinstance(entry["app_name"], str):
            raise ConfigValidationError(
                f"Profile '{alias}' in '{file_path.name}' has invalid "
                f"'app_name': must be a string, got {type(entry['app_name']).__name__}"
            )

        if "platform" not in entry:
            raise ConfigValidationError(
                f"Profile '{alias}' in '{file_path.name}' is missing "
                f"required 'platform' field"
            )

        # Validate platform is a valid Platform enum value
        platform_str = entry["platform"]
        try:
            platform = Platform(platform_str)
        except ValueError:
            valid_values = [p.value for p in Platform]
            raise ConfigValidationError(
                f"Profile '{alias}' in '{file_path.name}' has invalid "
                f"'platform' value '{platform_str}': must be one of {valid_values}"
            )

        result[alias] = Profile(
            alias=alias,
            app_key=str(entry["app_key"]),
            app_name=str(entry["app_name"]),
            platform=platform,
        )

    return result


