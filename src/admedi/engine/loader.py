def load_tiers_definition(
    settings_dir: str = "settings",
    country_groups: dict[str, list[str]] | None = None,
) -> dict[str, str]:
    """Load and validate portfolio-wide tier definitions from ``tiers.yaml``.

    Reads ``tiers.yaml`` from the project root (resolved as
    ``Path(settings_dir).parent``, same convention as
    ``load_country_groups()``). The file is a YAML mapping of tier name to
    country group reference (a string -- either a group name defined in
    ``countries.yaml`` or the ``'*'`` literal for a catch-all tier).

    The returned dict maps tier name to country group reference string.
    Resolution to actual country codes happens later at
    ``resolve_app_tiers()`` time.

    If ``country_groups`` is not provided, ``load_country_groups()`` is
    called internally to load them from ``countries.yaml``.

    Args:
        settings_dir: Path to the settings directory. The parent of this
            path is treated as the project root containing
            ``tiers.yaml`` and ``countries.yaml``.
        country_groups: Pre-loaded country groups dict. If ``None``,
            ``load_country_groups()`` is called to load them.

    Returns:
        A dict mapping tier name (str) to country group reference (str).

    Raises:
        FileNotFoundError: If ``tiers.yaml`` does not exist.
        ConfigValidationError: If the YAML is malformed, empty, not a
            mapping, contains fewer than one tier, has non-string values,
            references a non-existent country group, or has two tiers
            resolving to the same country set.

    Examples:
        >>> tiers = load_tiers_definition("settings")  # doctest: +SKIP
        >>> tiers["Tier 1"]  # doctest: +SKIP
        'US'
        >>> tiers["All Countries"]  # doctest: +SKIP
        '*'
    """
    project_root = Path(settings_dir).parent
    file_path = project_root / "tiers.yaml"

    if not file_path.exists():
        raise FileNotFoundError(
            f"Tiers definition file not found: {file_path}"
        )

    yaml = YAML(typ="safe")

    try:
        raw_data = yaml.load(file_path)
    except YAMLError as exc:
        raise ConfigValidationError(
            f"Malformed YAML in '{file_path.name}': {exc}",
            detail=str(exc),
        ) from exc

    if raw_data is None:
        raise ConfigValidationError(
            f"Tiers definition file '{file_path.name}' is empty or contains no YAML data"
        )

    if not isinstance(raw_data, dict):
        raise ConfigValidationError(
            f"Tiers definition file '{file_path.name}' must contain a YAML mapping "
            f"(got {type(raw_data).__name__})"
        )

    if len(raw_data) == 0:
        raise ConfigValidationError(
            f"Tiers definition file '{file_path.name}' must define at least one tier"
        )

    # Load country groups if not provided
    if country_groups is None:
        country_groups = load_country_groups(settings_dir=settings_dir)

    # Validate each tier entry
    result: dict[str, str] = {}
    # Track resolved country sets (as frozensets) to detect duplicates.
    # The '*' catch-all tier is excluded from this check.
    resolved_sets: dict[frozenset[str], str] = {}

    for tier_name, group_ref in raw_data.items():
        # Each tier value must be a string
        if not isinstance(group_ref, str):
            raise ConfigValidationError(
                f"Tier '{tier_name}' in '{file_path.name}' must reference a "
                f"country group name (string), got {type(group_ref).__name__}"
            )

        # '*' is a valid catch-all -- skip group resolution check
        if group_ref != "*":
            if group_ref not in country_groups:
                raise ConfigValidationError(
                    f"Tier '{tier_name}' in '{file_path.name}' references "
                    f"country group '{group_ref}' which does not exist in "
                    f"countries.yaml"
                )

            # Check for duplicate resolved country sets
            resolved = frozenset(country_groups[group_ref])
            if resolved in resolved_sets:
                existing_tier = resolved_sets[resolved]
                raise ConfigValidationError(
                    f"Tiers '{existing_tier}' and '{tier_name}' in "
                    f"'{file_path.name}' resolve to the same country set "
                    f"(via groups '{country_groups[group_ref]}'); "
                    f"each tier must have a unique country set"
                )
            resolved_sets[resolved] = tier_name

        result[tier_name] = group_ref

    return result


