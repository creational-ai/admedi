def resolve_app_tiers(
    alias: str, settings_dir: str = "settings"
) -> list[PortfolioTier]:
    """Resolve per-app settings through the two-layer config chain.

    Reads ``{settings_dir}/{alias}.yaml`` in the per-app format (``alias``
    field + ad format sections, each a list of ``display_name: group_ref``
    mappings), then resolves country group references via ``countries.yaml``
    to produce a flat ``list[PortfolioTier]``.

    Each format-tier combination produces one ``PortfolioTier`` (Option A):
    simpler logic, supports per-format position differences, and the
    differ's ``_build_template_format_map()`` already handles single-format
    tiers.

    Resolution pipeline:
        1. Parse ``settings/{alias}.yaml`` -- ``alias`` field + format sections
        2. Validate ``alias`` field matches caller's ``alias`` parameter
        3. Validate ``alias`` exists in ``profiles.yaml``
        4. Resolve country group references via ``load_country_groups()``
        5. For each format section, for each entry (position = index + 1):
           create ``PortfolioTier(name, countries, position, [format], is_default)``

    Args:
        alias: App alias used to locate the per-app settings file
            (e.g., ``"hexar-ios"`` reads ``settings/hexar-ios.yaml``).
        settings_dir: Directory containing per-app settings files.
            Root-level files (``countries.yaml``, ``profiles.yaml``) are
            resolved from ``Path(settings_dir).parent``.

    Returns:
        A list of ``PortfolioTier`` instances, one per format-tier combination.

    Raises:
        FileNotFoundError: If the per-app settings file does not exist.
        ConfigValidationError: If the YAML is malformed, the ``alias``
            field is missing or mismatched, a country group reference is
            not found in ``countries.yaml``, or a format section key is
            invalid.

    Examples:
        >>> tiers = resolve_app_tiers("hexar-ios")  # doctest: +SKIP
        >>> len(tiers)  # doctest: +SKIP
        6
        >>> tiers[0].name  # doctest: +SKIP
        'Tier 1'
        >>> tiers[0].ad_formats  # doctest: +SKIP
        [<AdFormat.REWARDED: 'rewarded'>]
    """
    settings_path = Path(settings_dir)
    file_path = settings_path / f"{alias}.yaml"

    if not file_path.exists():
        raise FileNotFoundError(
            f"Per-app settings file not found: {file_path}"
        )

    yaml = YAML(typ="safe")

    try:
        raw_data = yaml.load(file_path)
    except YAMLError as exc:
        raise ConfigValidationError(
            f"Malformed YAML in '{file_path.name}': {exc}",
            detail=str(exc),
        ) from exc

    if raw_data is None or not isinstance(raw_data, dict):
        raise ConfigValidationError(
            f"Per-app settings file '{file_path.name}' must contain a YAML mapping"
        )

    # --- Validate alias field ---
    if "alias" not in raw_data:
        raise ConfigValidationError(
            f"Per-app settings file '{file_path.name}' is missing required "
            f"'alias' field"
        )

    file_alias = raw_data["alias"]
    if not isinstance(file_alias, str):
        raise ConfigValidationError(
            f"'alias' field in '{file_path.name}' must be a string, "
            f"got {type(file_alias).__name__}"
        )

    if file_alias != alias:
        raise ConfigValidationError(
            f"Alias mismatch in '{file_path.name}': file says "
            f"alias '{file_alias}' but resolve_app_tiers() was called "
            f"with alias '{alias}'"
        )

    # --- Validate alias exists in profiles.yaml ---
    project_root = settings_path.parent
    profiles_path = project_root / "profiles.yaml"
    profiles = load_profiles(profiles_path)
    if alias not in profiles:
        available = ", ".join(sorted(profiles.keys()))
        raise ConfigValidationError(
            f"Alias '{alias}' not found in profiles.yaml. "
            f"Available profiles: {available}"
        )

    # --- Load country group definitions ---
    country_groups = load_country_groups(settings_dir=settings_dir)

    # --- Parse format sections and resolve entries ---
    result: list[PortfolioTier] = []

    for key, value in raw_data.items():
        # Skip metadata keys (currently only 'alias')
        if key == "alias":
            continue

        # Validate format key
        if key not in _VALID_FORMAT_VALUES:
            raise ConfigValidationError(
                f"Invalid format section '{key}' in '{file_path.name}': "
                f"must be one of {sorted(_VALID_FORMAT_VALUES)}"
            )

        ad_format = AdFormat(key)

        # Value must be a list of entries (or empty list)
        if not isinstance(value, list):
            raise ConfigValidationError(
                f"Format section '{key}' in '{file_path.name}' must be "
                f"a list of tier names, got {type(value).__name__}"
            )

        for position, entry in enumerate(value, start=1):
            # Entry must be a single-key dict (display_name: group_ref)
            if isinstance(entry, str):
                raise ConfigValidationError(
                    f"Tier entry in format section '{key}' of '{file_path.name}' "
                    f"must be a mapping (e.g., 'Tier 1: US'), got plain string '{entry}'. "
                    f"Run 'admedi pull --app {alias}' to regenerate settings."
                )
            if not isinstance(entry, dict) or len(entry) != 1:
                raise ConfigValidationError(
                    f"Tier entry in format section '{key}' of '{file_path.name}' "
                    f"must be a single-key mapping (e.g., 'Tier 1: US')"
                )

            display_name = next(iter(entry))
            entry_value = entry[display_name]

            # Entry value must be a dict: {countries: ref, networks: ref}
            if not isinstance(entry_value, dict):
                raise ConfigValidationError(
                    f"Tier entry '{display_name}' in format section '{key}' "
                    f"of '{file_path.name}' must be a dict "
                    f"(e.g., '{{countries: US, networks: bidding-6}}'). "
                    f"Run 'admedi pull --app {alias}' to regenerate settings."
                )

            if "countries" not in entry_value:
                raise ConfigValidationError(
                    f"Tier entry '{display_name}' in format section '{key}' "
                    f"of '{file_path.name}' is missing required 'countries' key"
                )

            group_ref = entry_value["countries"]
            network_ref = entry_value.get("networks")

            # Resolve country group reference -> country codes
            if group_ref == "*":
                countries = ["*"]
                is_default = True
            else:
                if group_ref not in country_groups:
                    raise ConfigValidationError(
                        f"Country group '{group_ref}' (referenced by "
                        f"'{display_name}' in format section '{key}') "
                        f"not found in countries.yaml"
                    )
                countries = country_groups[group_ref]
                is_default = False

            result.append(
                PortfolioTier(
                    name=display_name,
                    countries=countries,
                    position=position,
                    is_default=is_default,
                    ad_formats=[ad_format],
                    network_preset=network_ref,
                )
            )

    return result


