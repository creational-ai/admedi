"""Tests for the snapshot generator (engine/snapshot.py).

Covers:
- Snapshot from mock Group list with instances
- Empty group list
- Groups with no instances
- Correct field mapping (simplified names)
- YAML header comments present
- Groups organized by ad_format
- Instance rate omission when None
- Platform inclusion when provided
"""

from __future__ import annotations

from io import StringIO

from ruamel.yaml import YAML

from admedi.engine.snapshot import generate_snapshot
from admedi.models.enums import AdFormat, Platform
from admedi.models.group import Group
from admedi.models.instance import Instance


def _make_instance(
    instance_id: int = 1,
    name: str = "ironSource Default",
    network: str = "ironSource",
    is_bidder: bool = True,
    group_rate: float | None = 0.5,
) -> Instance:
    """Helper to create an Instance for testing."""
    return Instance.model_validate({
        "id": instance_id,
        "name": name,
        "networkName": network,
        "isBidder": is_bidder,
        "groupRate": group_rate,
    })


def _make_group(
    group_name: str = "US Tier 1",
    ad_format: str = "interstitial",
    countries: list[str] | None = None,
    position: int = 1,
    group_id: int | None = 100,
    floor_price: float | None = None,
    ab_test: str | None = None,
    instances: list[Instance] | None = None,
) -> Group:
    """Helper to create a Group for testing."""
    data: dict = {
        "groupName": group_name,
        "adFormat": ad_format,
        "countries": countries or ["US"],
        "position": position,
    }
    if group_id is not None:
        data["groupId"] = group_id
    if floor_price is not None:
        data["floorPrice"] = floor_price
    if ab_test is not None:
        data["abTest"] = ab_test
    if instances is not None:
        data["instances"] = [
            inst.model_dump(by_alias=True) for inst in instances
        ]
    return Group.model_validate(data)


def _parse_yaml(yaml_str: str) -> dict:
    """Parse a YAML string, stripping header comments."""
    yaml = YAML(typ="safe")
    return yaml.load(StringIO(yaml_str))


class TestSnapshotHeader:
    """Tests for YAML header comments."""

    def test_header_contains_generated_by(self) -> None:
        """Header includes the 'Generated by admedi snapshot' line."""
        result = generate_snapshot([], "key1", "Test App")
        assert "# Generated by admedi snapshot" in result

    def test_header_contains_app_info(self) -> None:
        """Header includes app name and key."""
        result = generate_snapshot([], "abc123", "My Game")
        assert "# App: My Game (abc123)" in result

    def test_header_contains_captured_timestamp(self) -> None:
        """Header includes a Captured timestamp line."""
        result = generate_snapshot([], "key1", "Test App")
        lines = result.split("\n")
        captured_lines = [l for l in lines if l.startswith("# Captured:")]
        assert len(captured_lines) == 1
        # Timestamp should have ISO-like format with 'T' separator
        timestamp_part = captured_lines[0].replace("# Captured: ", "")
        assert "T" in timestamp_part
        assert timestamp_part.endswith("Z")

    def test_header_order(self) -> None:
        """Header comments appear in the correct order."""
        result = generate_snapshot([], "key1", "Test App")
        lines = result.split("\n")
        comment_lines = [l for l in lines if l.startswith("#")]
        assert len(comment_lines) == 3
        assert comment_lines[0] == "# Generated by admedi snapshot"
        assert comment_lines[1].startswith("# App:")
        assert comment_lines[2].startswith("# Captured:")


