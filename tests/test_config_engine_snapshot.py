class TestSnapshotRoundTrip:
    """Tests for full round-trip YAML validity."""

    def test_full_snapshot_parseable(self) -> None:
        """A full snapshot with multiple groups and instances is valid YAML."""
        inst1 = _make_instance(instance_id=1, name="IS Default", network="ironSource", group_rate=0.5)
        inst2 = _make_instance(instance_id=2, name="AdMob Banner", network="AdMob", group_rate=None)

        groups = [
            _make_group(
                "US Tier 1", "interstitial", ["US"], 1,
                group_id=100, floor_price=2.0, ab_test=None,
                instances=[inst1, inst2],
            ),
            _make_group(
                "EU Tier 2", "interstitial", ["DE", "FR", "GB"], 2,
                group_id=101, floor_price=None, ab_test="exp_1",
                instances=[inst1],
            ),
            _make_group(
                "US Rewarded", "rewarded", ["US"], 1,
                group_id=200, instances=[],
            ),
        ]

        result = generate_snapshot(groups, "abc123", "Shelf Sort", platform=Platform.IOS)
        data = _parse_yaml(result)

        # Top-level
        assert data["app_key"] == "abc123"
        assert data["app_name"] == "Shelf Sort"
        assert data["platform"] == "iOS"

        # Groups by format
        assert "interstitial" in data["groups"]
        assert "rewarded" in data["groups"]
        assert len(data["groups"]["interstitial"]) == 2
        assert len(data["groups"]["rewarded"]) == 1

        # First interstitial group detail
        g1 = data["groups"]["interstitial"][0]
        assert g1["name"] == "US Tier 1"
        assert g1["group_id"] == 100
        assert g1["countries"] == ["US"]
        assert g1["position"] == 1
        assert g1["floor_price"] == 2.0
        assert g1["ab_test"] is None
        assert len(g1["instances"]) == 2
        assert g1["instances"][0]["rate"] == 0.5
        assert "rate" not in g1["instances"][1]  # None rate omitted

        # Second interstitial group
        g2 = data["groups"]["interstitial"][1]
        assert g2["name"] == "EU Tier 2"
        assert g2["ab_test"] == "exp_1"
        assert len(g2["instances"]) == 1

        # Rewarded group (no instances)
        g3 = data["groups"]["rewarded"][0]
        assert g3["name"] == "US Rewarded"
        assert g3["instances"] == []

    def test_group_id_none_serialized(self) -> None:
        """Groups with group_id=None serialize correctly."""
        groups = [_make_group("New Group", "banner", group_id=None)]
        result = generate_snapshot(groups, "key1", "Test App")
        data = _parse_yaml(result)
        group_data = data["groups"]["banner"][0]
        assert group_data["group_id"] is None


