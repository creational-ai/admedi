class TestSnapshotGroupOrganization:
    """Tests for groups organized by ad_format."""

    def test_single_format(self) -> None:
        """Groups of a single format are organized under that format key."""
        groups = [
            _make_group("US Tier 1", "interstitial", ["US"], 1),
            _make_group("EU Tier 2", "interstitial", ["DE", "FR"], 2),
        ]
        result = generate_snapshot(groups, "key1", "Test App")
        data = _parse_yaml(result)
        assert "interstitial" in data["groups"]
        assert len(data["groups"]["interstitial"]) == 2

    def test_multiple_formats(self) -> None:
        """Groups of different formats are organized under separate keys."""
        groups = [
            _make_group("US Tier 1", "interstitial", ["US"], 1),
            _make_group("US Rewarded", "rewarded", ["US"], 1),
            _make_group("Banner All", "banner", ["US"], 1),
        ]
        result = generate_snapshot(groups, "key1", "Test App")
        data = _parse_yaml(result)
        assert "interstitial" in data["groups"]
        assert "rewarded" in data["groups"]
        assert "banner" in data["groups"]
        assert len(data["groups"]["interstitial"]) == 1
        assert len(data["groups"]["rewarded"]) == 1
        assert len(data["groups"]["banner"]) == 1

    def test_groups_with_no_instances(self) -> None:
        """Groups with no instances have an empty instances list."""
        groups = [_make_group("US Tier 1", "interstitial")]
        result = generate_snapshot(groups, "key1", "Test App")
        data = _parse_yaml(result)
        group_data = data["groups"]["interstitial"][0]
        assert group_data["instances"] == []


