def load_country_groups(settings_dir: str = "settings") -> dict[str, list[str]]:
    """Load and validate portfolio-wide country group definitions.

    Reads ``countries.yaml`` from the project root (resolved as
    ``Path(settings_dir).parent``). The file is a simple YAML mapping of
    group name to country code list.

    Convention: ``settings_dir`` must be structured so ``.parent`` yields
    the project root containing ``countries.yaml``. The default
    ``"settings"`` works when CWD is the project root; CLI callers should
    pass absolute paths for robustness.

    Args:
        settings_dir: Path to the settings directory. The parent of this
            path is treated as the project root containing
            ``countries.yaml``.

    Returns:
        A dict mapping group name (str) to a list of 2-letter uppercase
        country codes (list[str]).

    Raises:
        FileNotFoundError: If ``countries.yaml`` does not exist.
        ConfigValidationError: If the YAML is malformed, empty, not a
            mapping, or contains invalid country codes.

    Examples:
        >>> groups = load_country_groups("settings")  # doctest: +SKIP
        >>> groups["US"]  # doctest: +SKIP
        ['US']
        >>> "AU" in groups["high-value"]  # doctest: +SKIP
        True
    """
    project_root = Path(settings_dir).parent
    file_path = project_root / "countries.yaml"

    if not file_path.exists():
        raise FileNotFoundError(
            f"Country groups file not found: {file_path}"
        )

    yaml = YAML(typ="safe")

    try:
        raw_data = yaml.load(file_path)
    except YAMLError as exc:
        raise ConfigValidationError(
            f"Malformed YAML in '{file_path.name}': {exc}",
            detail=str(exc),
        ) from exc

    if raw_data is None:
        raise ConfigValidationError(
            f"Country groups file '{file_path.name}' is empty or contains no YAML data"
        )

    if not isinstance(raw_data, dict):
        raise ConfigValidationError(
            f"Country groups file '{file_path.name}' must contain a YAML mapping "
            f"(got {type(raw_data).__name__})"
        )

    result: dict[str, list[str]] = {}

    for group_name, country_list in raw_data.items():
        # Each value must be a non-empty list
        if not isinstance(country_list, list) or len(country_list) == 0:
            raise ConfigValidationError(
                f"Country group '{group_name}' in '{file_path.name}' "
                f"must be a non-empty list of country codes"
            )

        # Check for duplicates within this group
        seen: set[str] = set()
        for code in country_list:
            if not isinstance(code, str):
                raise ConfigValidationError(
                    f"Invalid country code {code!r} in group '{group_name}' "
                    f"of '{file_path.name}': must be a string"
                )

            if not _COUNTRY_CODE_PATTERN.match(code):
                raise ConfigValidationError(
                    f"Invalid country code '{code}' in group '{group_name}' "
                    f"of '{file_path.name}': must be 2 uppercase letters (e.g., 'US')"
                )

            if code in seen:
                raise ConfigValidationError(
                    f"Duplicate country code '{code}' in group '{group_name}' "
                    f"of '{file_path.name}'"
                )
            seen.add(code)

        result[group_name] = list(country_list)

    return result


