"""YAML template loader for portfolio tier configs and settings files.

Reads YAML files from disk, parses them via ``ruamel.yaml`` (safe mode),
and validates the results into Pydantic models.

Seven loaders are provided:

1. ``load_template()`` -- Reads a full portfolio tier template (with
   ``schema_version``, ``mediator``, ``portfolio``, and ``tiers`` keys)
   and returns a ``PortfolioConfig``.

2. ``load_tiers_settings()`` -- (Legacy) Reads a tiers settings file
   (generated by ``admedi pull``) with a ``tiers`` dict mapping tier
   names to their country lists and format associations, and returns a
   ``list[PortfolioTier]``.

3. ``load_country_groups()`` -- Reads a ``countries.yaml`` file defining
   portfolio-wide named country groups (group name -> country code list),
   validates country codes (``^[A-Z]{2}$``), and returns a dict.

4. ``load_tiers_definition()`` -- Reads a ``tiers.yaml`` file mapping
   tier names to country group references (strings), validates that
   references resolve in the country groups dict, and returns a dict.

5. ``load_profiles()`` -- Reads a ``profiles.yaml`` file mapping app
   aliases to expanded profile data (``app_key``, ``app_name``,
   ``platform``), and returns a dict of alias -> ``Profile``.

6. ``resolve_app_tiers()`` -- Two-layer resolution: reads a per-app
   settings file (``settings/{alias}.yaml``), resolves country group
   references via ``countries.yaml``, and returns a
   ``list[PortfolioTier]`` (one per format-tier combination).

7. ``load_network_presets()`` -- Reads a ``networks.yaml`` file defining
   portfolio-wide named network waterfall presets. Each preset maps to a
   list of instance entries with ``network`` (str) and ``bidder`` (bool)
   fields. Returns a ``dict[str, list[dict[str, Any]]]``.

The loader is synchronous (no async I/O). If the calling orchestrator
needs non-blocking behavior, it can wrap the loader in
``asyncio.to_thread()``.

Error handling strategy:
- ``FileNotFoundError`` propagates directly (file does not exist).
- ``ruamel.yaml.YAMLError`` is caught and re-raised as
  ``ConfigValidationError`` with a human-readable message.
- Pydantic ``ValidationError`` is caught and re-raised as
  ``ConfigValidationError`` with a human-readable message.

Examples:
    >>> from admedi.engine.loader import load_template, resolve_app_tiers
    >>> config = load_template("examples/shelf-sort-tiers.yaml")
    >>> config.mediator
    <Mediator.LEVELPLAY: 'levelplay'>
    >>> len(config.tiers)
    4
    >>> tiers = resolve_app_tiers("hexar-ios")
    >>> len(tiers)
    4
"""

from __future__ import annotations

import re
from pathlib import Path
from typing import Any

from pydantic import BaseModel, Field, ValidationError
from ruamel.yaml import YAML, YAMLError

from admedi.exceptions import ConfigValidationError
from admedi.models.enums import AdFormat, Platform
from admedi.models.portfolio import PortfolioConfig, PortfolioTier


